package Domain.CalculadorDistancia.Endpoints;

public class Provincia {
  private String id;
  private String nombre;

  // GETTERS
  public String getId() {
    return id;
  }
  public String getNombre() {
    return nombre;
  }

  // SETTER
  public void setId(String id) {
    this.id = id;
  }
  public void setNombre(String nombre) {
    this.nombre = nombre;
  }

}
